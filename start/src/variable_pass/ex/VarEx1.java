package variable_pass.ex;

public class VarEx1 {

    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 3;
        System.out.println(num1 + num2); // int 변수의 초기화된 값을 더하는 식 = 13
        System.out.println(num1 - num2); // 변수값을 빼는 식 10 - 3 = 7
        System.out.println(num1 * num2); // 곱하는 식 10 * 3 = 30
    }
}
